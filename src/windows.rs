use std::f64::consts::PI;

pub fn hann(k: usize, n: usize) -> f64 {
	let phi: f64 = 2. * PI * (k as f64) / ((n) as f64);
	0.5 * (1. - phi.cos())
}

pub fn rectangle(_k: usize, _n: usize) -> f64 {
	1.
}

	
